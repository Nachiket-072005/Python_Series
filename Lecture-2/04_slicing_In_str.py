# SLICING POSITIVE METHOD TO FIND OUT IN GIVEN STRING
str = "Hello World"
print(str[: 5]) # It gives whole string from first string to whatever string till the ending index. It can also be written as str[0 : 5]
print(str[6 :]) # It gives whole string from starting index. It can also be written as str[6 : len(str)]

# SLICING NEGATIVE METHOD TO FIND OUT IN GIVEN STRING
str = "Apple"
print(str[-3 : -1]) #It is similiar as positive method also.